# Function example

def greet(name): # Define a function named greet that takes one parameter, name
    print(f"Hello, {name}!") # Print a greeting message with the provided name
    

greet("Bishal") # Call the greet function with the argument "Bishal", which will print "Hello, Bishal!"
greet("Yubin") # Call the greet function with the argument "Yubin", which will print "Hello, Yubin!"
greet("Bishwash")
greet("Dhaval Kumar Wagle")


